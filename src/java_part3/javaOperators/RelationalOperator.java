package java_part3.javaOperators;

public class RelationalOperator {
    public static void main(String[] args) {
        int firstNumber= 300;
        int secondNumber= 150;
        //==operator
        System.out.println(firstNumber==secondNumber);
        //!= operator
        System.out.println(firstNumber!=secondNumber);

        //> operator
        System.out.println(firstNumber>secondNumber);
        //< operator
        System.out.println(firstNumber<secondNumber);

        //>= operator
        System.out.println(firstNumber>=secondNumber);
        //<= operator
        System.out.println(firstNumber<=secondNumber);

    }
}
