package java_part3.javaOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        //declare variable
        int number1=50, number2=12;
        //addition
        System.out.println("Addition value "+ (number1+number2));
        //substraction
        System.out.println("Substraction value: " +(number1-number2));
        //Multiplication
        System.out.println("Multiplication value: " +(number1* number2));
        //division

        System.out.println("Division value: " +(number1/ number2));

        //module
        System.out.println("module value: " +(number1 % number2));

    }
}
