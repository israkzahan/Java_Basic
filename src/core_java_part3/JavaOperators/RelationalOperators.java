package core_java_part3.JavaOperators;

public class RelationalOperators {
    public static void main(String[] args) {
        //create variable
        int firstNumber=300;
        int secondNumber=150;
        //==operator
        System.out.println(firstNumber==secondNumber);

        //!=operator
        System.out.println(firstNumber!=secondNumber);

        //> Operator
        System.out.println(firstNumber>secondNumber);

        //< Operator
        System.out.println(firstNumber<secondNumber);

        //>= Operator
        System.out.println(firstNumber>=secondNumber);

        //<=Operator
        System.out.println(firstNumber<=secondNumber);

    }
}
