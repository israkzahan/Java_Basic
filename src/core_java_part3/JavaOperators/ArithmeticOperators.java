package core_java_part3.JavaOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        //declare variables
        int firstNumber=800, secondNumber=200;
        //addition between two numbers
        System.out.println("First Number+ Second Number:" +(firstNumber+secondNumber));
        //subtraction
        System.out.println("First Number - Second Number:" +(firstNumber-secondNumber));
        //multiplication
        System.out.println("First Number * Second Number:" +(firstNumber*secondNumber));
        //division
        System.out.println("First Number / Second Number:" +(firstNumber/secondNumber));

    }
}
